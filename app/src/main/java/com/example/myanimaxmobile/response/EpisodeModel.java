package com.example.myanimaxmobile.response;

public class EpisodeModel {
    private String id_eps, anime_id, eps, link;
    public EpisodeModel(){
        this.id_eps = id_eps;
        this.anime_id = anime_id;
        this.eps = eps;
        this.link = link;
    }

    //getter
    public String getId_eps() {
        return id_eps;
    }
    public String getAnime_id() {
        return anime_id;
    }
    public String getEps() {
        return eps;
    }
    public String getLink() {
        return link;
    }
}
